public class Calculator {

    public int AddcalculationsOperations(int x, int y) { return x + y; }

    public int DiffcalculationsOperations(int x, int y) { return x - y; }

    public int MultcalculationsOperations(int x, int y) {
        return x * y;
    }

    public int DelcalculationsOperations(int x, int y) {
        return x / y;
    }
}
